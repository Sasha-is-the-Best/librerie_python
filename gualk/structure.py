def to(a, b):
    return a+b
def size(a, b):
    return a-b
def loop(a, b):
    return a*b
def tri(a, b):
    return a/b
def kal(a, b):
    return a%b
class Math:
    def __init__(self):
        self.to = to(0, 0)
        self.size = size(0, 0)
        self.loop = loop(0, 0)
        self.tri = tri(0, 1)
        self.kal = kal(0, 1)
    def to(to):
        return to.a+to.b
    def size(size):
        return size.a-size.b
    def loop(loop):
        return loop.a*loop.b
    def tri(tri):
        return tri.a/tri.b
    def kal(a, b):
        return kal.a%kal.b